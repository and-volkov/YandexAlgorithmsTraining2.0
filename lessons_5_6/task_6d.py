"""
Фермер Николай нанял двух лесорубов: Дмитрия и Федора,
чтобы вырубить лес, на месте которого должно быть кукурузное поле.
В лесу растут X деревьев.

Дмитрий срубает по A деревьев в день, но каждый K-й день он отдыхает
и не срубает ни одного дерева. Таким образом, Дмитрий отдыхает в K-й,
2K-й, 3K-й день, и т.д.

Федор срубает по B деревьев в день, но каждый M-й день он отдыхает
и не срубает ни одного дерева. Таким образом, Федор отдыхает в M-й,
2M-й, 3M-й день, и т.д.

Лесорубы работают параллельно и, таким образом, в дни, когда никто
из них не отдыхает, они срубают A + B деревьев, в дни, когда отдыхает
только Федор — A деревьев, а в дни, когда отдыхает только Дмитрий —
B деревьев. В дни, когда оба лесоруба отдыхают, ни одно дерево не
срубается.

Фермер Николай хочет понять, за сколько дней лесорубы срубят все
деревья, и он сможет засеять кукурузное поле.

Требуется написать программу, которая по заданным целым числам
A, K, B, M и X определяет,
за сколько дней все деревья в лесу будут вырублены.
"""

# после разбора

a, k, b, m, x = map(int, input().split())

left = 0
right = x * 2 // a + 1

while left < right:
    days = (left + right) // 2
    holidays_dmitry = days // k
    holidays_fedor = days // m
    lumber = (days - holidays_dmitry) * a + (days - holidays_fedor) * b
    if lumber < x:
        left = days + 1
    else:
        right = days

print(left)
